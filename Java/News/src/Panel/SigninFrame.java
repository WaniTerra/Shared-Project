    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        labelEmailError.setVisible(false);
        labelGenderError.setVisible(false);
        labelImageError.setVisible(false);
        labelNameError.setVisible(false);
        labelPasswordError.setVisible(false);
        labelSurnameError.setVisible(false);
        labelGenreError.setVisible(false);

        String gender = "";
        if (radioMan.isSelected()) {
            gender = "Man";
        } else if (radioWoman.isSelected()) {
            gender = "Woman";
        }
        int selected[] = listGenres.getSelectedIndices();
        ArrayList<String> genres = new ArrayList<>();

        for (int i = 0; i < selected.length; i++) {
            genres.add(listGenre.getElementAt(selected[i]).toString());
        }

        ArrayList<String> sıgnInResult = null;
        if(isAdmin){
            sıgnInResult = PublicMethods.sıgnInController(txtName.getText(), txtSurname.getText(),
                    txtEmail.getText(), new String(txtPassword.getPassword()), genres, isReader, gender,
                    comboFavorite.getSelectedItem().toString(), txtBiography.getText(),
                    DatabaseCodes.convertImageIconToByteArray(profilPhoto), true, comboFavorite.getSelectedItem().toString().trim());
        } else {
            sıgnInResult = PublicMethods.sıgnInController(txtName.getText(), txtSurname.getText(),
                    txtEmail.getText(), new String(txtPassword.getPassword()), genres, isReader, gender,
                    comboFavorite.getSelectedItem().toString(), txtBiography.getText(),
                    DatabaseCodes.convertImageIconToByteArray(profilPhoto), false, null);
        }
        //Error writer
        if (!sıgnInResult.isEmpty()) {
            for (int i = 0; i < sıgnInResult.size(); i++) {
                if (sıgnInResult.get(i).equals("name")) {
                    labelNameError.setVisible(true);
                }
                if (sıgnInResult.get(i).equals("surname")) {
                    labelSurnameError.setVisible(true);
                }
                if (sıgnInResult.get(i).equals("password")) {
                    labelPasswordError.setText("Fill the area");
                    labelPasswordError.setVisible(true);
                }
                if (sıgnInResult.get(i).equals("passwordSize")) {
                    labelPasswordError.setVisible(true);
                    labelPasswordError.setText("Size should above 8");
                }
                if (sıgnInResult.get(i).equals("email")) {
                    labelEmailError.setText("Email should contains @ .com");
                    labelEmailError.setVisible(true);
                }
                if (sıgnInResult.get(i).equals("emailExist")) {
                    labelEmailError.setText("This email already in use.");
                    labelEmailError.setVisible(true);
                }

                if (sıgnInResult.get(i).equals("gender")) {
                    labelGenderError.setVisible(true);
                }
                if (sıgnInResult.get(i).equals("genres")) {
                    labelGenreError.setVisible(true);
                }

                if (sıgnInResult.get(i).equals("image")) {
                    labelImageError.setVisible(true);
                }

            }
        } else {
            if (isReader) {
                LoginFrame logIn = new LoginFrame();
                logIn.setVisible(true);
                logIn.setLocationRelativeTo(this);
            } else if (!isReader && !isAdmin) {
                AdminMainFrame amf = new AdminMainFrame();
                amf.setVisible(true);
                amf.setLocationRelativeTo(this);
            } else if (isAdmin){
                AdminFrame af = new AdminFrame();
                af.setVisible(true);
                af.setLocationRelativeTo(this);
            }
            this.dispose();
        }


    }//GEN-LAST:event_registerActionPerformed

