    private void btnProfilPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProfilPhotoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Choose a visual");
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
                "Resim Dosyaları", "jpg", "jpeg", "png", "gif", "bmp"
        ));

        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            NewsFrame nf = new NewsFrame("size");
            JLabel labelSize = nf.getLabelImageIcon();
            ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());
            if ((imageIcon.getIconHeight() > labelSize.getBounds().height)
                    || (imageIcon.getIconWidth() > labelSize.getBounds().width)) {
                Image scaledImage = imageIcon.getImage().getScaledInstance(
                        labelSize.getWidth(),
                        labelSize.getHeight(),
                        Image.SCALE_SMOOTH
                );
                btnProfilPhoto.setText("");
                btnProfilPhoto.setIcon(new ImageIcon(scaledImage));
                profilPhoto = new ImageIcon(scaledImage);

            } else {
                btnProfilPhoto.setText("");
                btnProfilPhoto.setIcon(imageIcon);
                profilPhoto = imageIcon;
            }

        }
    }//GEN-LAST:event_btnProfilPhotoActionPerformed

